class AttnProcessor(nn.Module):
    r"""
    Default processor for performing attention-related computations.
    """

    def __init__(
        self,
        hidden_size=None,
        cross_attention_dim=None,
    ):
        super().__init__()

    def __call__(
        self,
        attn,
        hidden_states,
        encoder_hidden_states=None,
        attention_mask=None,
        temb=None,
    ):
        residual = hidden_states

        if attn.spatial_norm is not None:
            hidden_states = attn.spatial_norm(hidden_states, temb)

        input_ndim = hidden_states.ndim

        if input_ndim == 4:
            batch_size, channel, height, width = hidden_states.shape
            hidden_states = hidden_states.view(batch_size, channel, height * width).transpose(1, 2)

        batch_size, sequence_length, _ = (
            hidden_states.shape if encoder_hidden_states is None else encoder_hidden_states.shape
        )
        attention_mask = attn.prepare_attention_mask(attention_mask, sequence_length, batch_size)

        if attn.group_norm is not None:
            hidden_states = attn.group_norm(hidden_states.transpose(1, 2)).transpose(1, 2)

        query = attn.to_q(hidden_states)

        if encoder_hidden_states is None:
            encoder_hidden_states = hidden_states
        elif attn.norm_cross:
            encoder_hidden_states = attn.norm_encoder_hidden_states(encoder_hidden_states)

        key = attn.to_k(encoder_hidden_states)
        value = attn.to_v(encoder_hidden_states)

        query = attn.head_to_batch_dim(query)
        key = attn.head_to_batch_dim(key)
        value = attn.head_to_batch_dim(value)

        attention_probs = attn.get_attention_scores(query, key, attention_mask)
        hidden_states = torch.bmm(attention_probs, value)
        hidden_states = attn.batch_to_head_dim(hidden_states)

        # linear proj
        hidden_states = attn.to_out[0](hidden_states)
        # dropout
        hidden_states = attn.to_out[1](hidden_states)

        if input_ndim == 4:
            hidden_states = hidden_states.transpose(-1, -2).reshape(batch_size, channel, height, width)

        if attn.residual_connection:
            hidden_states = hidden_states + residual

        hidden_states = hidden_states / attn.rescale_output_factor

        return hidden_states


class IPAttnProcessor(nn.Module):
    r"""
    Attention processor for IP-Adapater.
    Args:
        hidden_size (`int`):
            The hidden size of the attention layer.
        cross_attention_dim (`int`):
            The number of channels in the `encoder_hidden_states`.
        scale (`float`, defaults to 1.0):
            the weight scale of image prompt.
        num_tokens (`int`, defaults to 4 when do ip_adapter_plus it should be 16):
            The context length of the image features.
    """

    def __init__(self, hidden_size, cross_attention_dim=None, scale=1.0, num_tokens=4):
        super().__init__()

        self.hidden_size = hidden_size
        self.cross_attention_dim = cross_attention_dim
        self.scale = scale
        self.num_tokens = num_tokens

        self.to_k_ip = nn.Linear(cross_attention_dim or hidden_size, hidden_size, bias=False)
        self.to_v_ip = nn.Linear(cross_attention_dim or hidden_size, hidden_size, bias=False)

        # --- 新增修改: 可学习的融合参数 ---
        self.fusion_alpha = nn.Parameter(torch.tensor(0.5)) # 初始化，后续模型会学习
        # --- 新增修改: IP Attention 温度参数 ---
        self.ip_temperature = nn.Parameter(torch.tensor(1.0)) # 初始化为1.0 (不起作用)，后续模型会学习

    def __call__(
        self,
        attn,
        hidden_states,
        encoder_hidden_states=None,
        attention_mask=None,
        temb=None,
    ):
        residual = hidden_states

        if attn.spatial_norm is not None:
            hidden_states = attn.spatial_norm(hidden_states, temb)

        input_ndim = hidden_states.ndim

        if input_ndim == 4:
            batch_size, channel, height, width = hidden_states.shape
            hidden_states = hidden_states.view(batch_size, channel, height * width).transpose(1, 2)

        batch_size, sequence_length, _ = (
            hidden_states.shape if encoder_hidden_states is None else encoder_hidden_states.shape
        )
        attention_mask = attn.prepare_attention_mask(attention_mask, sequence_length, batch_size)

        if attn.group_norm is not None:
            hidden_states = attn.group_norm(hidden_states.transpose(1, 2)).transpose(1, 2)

        query = attn.to_q(hidden_states)
        # query: (batch_heads, q_seq_len, head_dim) after head_to_batch_dim

        if encoder_hidden_states is None:
            encoder_hidden_states = hidden_states
        else:
            # get encoder_hidden_states, ip_hidden_states
            end_pos = encoder_hidden_states.shape[1] - self.num_tokens
            encoder_hidden_states, ip_hidden_states = (
                encoder_hidden_states[:, :end_pos, :],
                encoder_hidden_states[:, end_pos:, :],
            )
            if attn.norm_cross:
                encoder_hidden_states = attn.norm_encoder_hidden_states(encoder_hidden_states)

        key = attn.to_k(encoder_hidden_states)
        value = attn.to_v(encoder_hidden_states)

        query = attn.head_to_batch_dim(query) # (batch_size * num_heads, q_sequence_length, dim_per_head)
        key = attn.head_to_batch_dim(key)     # (batch_size * num_heads, kv_sequence_length, dim_per_head)
        value = attn.head_to_batch_dim(value) # (batch_size * num_heads, kv_sequence_length, dim_per_head)

        attention_probs = attn.get_attention_scores(query, key, attention_mask)
        hidden_states = torch.bmm(attention_probs, value)
        hidden_states = attn.batch_to_head_dim(hidden_states)

        # for ip-adapter
        ip_key_orig = self.to_k_ip(ip_hidden_states) # (batch_size, num_ip_tokens, hidden_size)
        ip_value_orig = self.to_v_ip(ip_hidden_states) # (batch_size, num_ip_tokens, hidden_size)

        ip_key = attn.head_to_batch_dim(ip_key_orig) # (batch_size * num_heads, num_ip_tokens, dim_per_head)
        ip_value = attn.head_to_batch_dim(ip_value_orig) # (batch_size * num_heads, num_ip_tokens, dim_per_head)

        # --- 修改: 手动计算 IP Attention 并加入温度 ---
        # query: (batch_size * num_heads, q_sequence_length, dim_per_head)
        # ip_key: (batch_size * num_heads, num_ip_tokens, dim_per_head)
        ip_attention_scores = torch.bmm(query, ip_key.transpose(1, 2))

        # Apply scaling similar to how original attention does (e.g., 1/sqrt(dim_per_head))
        # attn.scale is often 1.0 in AttnProcessor's direct call, scaling might be inside get_attention_scores
        # Or if attn.scale is defined as the scaling factor (e.g. 1/sqrt(dim_k))
        if hasattr(attn, 'scale') and attn.scale is not None and not isinstance(attn.scale, torch.Tensor): # diffusers attn often has self.scale = head_dim**-0.5
             ip_attention_scores *= attn.scale
        else:
            head_dim = query.shape[-1]
            ip_attention_scores /= math.sqrt(head_dim)

        effective_temperature = torch.clamp(self.ip_temperature, min=1e-2) # 避免除以0或负数，或过小的值
        ip_attention_probs = F.softmax(ip_attention_scores / effective_temperature, dim=-1)
        # --- 结束修改 ---

        ip_hidden_states_out = torch.bmm(ip_attention_probs, ip_value)
        ip_hidden_states_out = attn.batch_to_head_dim(ip_hidden_states_out)

        # --- 修改: 使用可学习的融合权重 ---
        learned_scale_factor = torch.sigmoid(self.fusion_alpha) * self.scale # 让 self.scale 作为上限或基准
        # learned_scale_factor = self.fusion_alpha # 或者直接用 fusion_alpha，但要注意其范围
        hidden_states = hidden_states + learned_scale_factor * ip_hidden_states_out
        # --- 结束修改 ---

        # linear proj
        hidden_states = attn.to_out[0](hidden_states)
        # dropout
        hidden_states = attn.to_out[1](hidden_states)

        if input_ndim == 4:
            hidden_states = hidden_states.transpose(-1, -2).reshape(batch_size, channel, height, width)

        if attn.residual_connection:
            hidden_states = hidden_states + residual

        hidden_states = hidden_states / attn.rescale_output_factor

        return hidden_states


