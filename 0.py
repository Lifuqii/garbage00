class IPAttnProcessor2_0(torch.nn.Module):
    r"""
    Attention processor for IP-Adapater for PyTorch 2.0.
    Args:
        hidden_size (`int`):
            The hidden size of the attention layer.
        cross_attention_dim (`int`):
            The number of channels in the `encoder_hidden_states`.
        scale (`float`, defaults to 1.0):
            the weight scale of image prompt.
        num_tokens (`int`, defaults to 4 when do ip_adapter_plus it should be 16):
            The context length of the image features.
    """

    def __init__(self, hidden_size, cross_attention_dim=None, scale=1.0, num_tokens=4):
        super().__init__()

        if not hasattr(F, "scaled_dot_product_attention"):
            raise ImportError("AttnProcessor2_0 requires PyTorch 2.0, to use it, please upgrade PyTorch to 2.0.")

        self.hidden_size = hidden_size
        self.cross_attention_dim = cross_attention_dim
        self.scale = scale
        self.num_tokens = num_tokens

        self.to_k_ip = nn.Linear(cross_attention_dim or hidden_size, hidden_size, bias=False)
        self.to_v_ip = nn.Linear(cross_attention_dim or hidden_size, hidden_size, bias=False)

        # --- 新增修改: 可学习的融合参数 ---
        self.fusion_alpha = nn.Parameter(torch.tensor(0.5))
        # --- 新增修改: IP Attention 温度参数 ---
        self.ip_temperature = nn.Parameter(torch.tensor(1.0))


    def __call__(
        self,
        attn,
        hidden_states,
        encoder_hidden_states=None,
        attention_mask=None,
        temb=None,
    ):
        residual = hidden_states

        if attn.spatial_norm is not None:
            hidden_states = attn.spatial_norm(hidden_states, temb)

        input_ndim = hidden_states.ndim

        if input_ndim == 4:
            batch_size, channel, height, width = hidden_states.shape
            hidden_states = hidden_states.view(batch_size, channel, height * width).transpose(1, 2)

        batch_size, sequence_length, _ = (
            hidden_states.shape if encoder_hidden_states is None else encoder_hidden_states.shape
        )

        if attention_mask is not None:
            attention_mask = attn.prepare_attention_mask(attention_mask, sequence_length, batch_size)
            # scaled_dot_product_attention expects attention_mask shape to be
            # (batch, heads, source_length, target_length)
            attention_mask = attention_mask.view(batch_size, attn.heads, -1, attention_mask.shape[-1])

        if attn.group_norm is not None:
            hidden_states = attn.group_norm(hidden_states.transpose(1, 2)).transpose(1, 2)

        query = attn.to_q(hidden_states)

        if encoder_hidden_states is None:
            encoder_hidden_states = hidden_states
        else:
            # get encoder_hidden_states, ip_hidden_states
            end_pos = encoder_hidden_states.shape[1] - self.num_tokens
            encoder_hidden_states_text, ip_hidden_states_clip = ( # Renamed to avoid confusion
                encoder_hidden_states[:, :end_pos, :],
                encoder_hidden_states[:, end_pos:, :],
            )
            if attn.norm_cross:
                encoder_hidden_states_text = attn.norm_encoder_hidden_states(encoder_hidden_states_text)

        key = attn.to_k(encoder_hidden_states_text) # Use text features for K
        value = attn.to_v(encoder_hidden_states_text) # Use text features for V

        inner_dim = key.shape[-1]
        head_dim = inner_dim // attn.heads

        # query for text attention
        query_text = query.view(batch_size, -1, attn.heads, head_dim).transpose(1, 2)
        key_text = key.view(batch_size, -1, attn.heads, head_dim).transpose(1, 2)
        value_text = value.view(batch_size, -1, attn.heads, head_dim).transpose(1, 2)

        hidden_states_text = F.scaled_dot_product_attention(
            query_text, key_text, value_text, attn_mask=attention_mask, dropout_p=0.0, is_causal=False
        )
        hidden_states_text = hidden_states_text.transpose(1, 2).reshape(batch_size, -1, attn.heads * head_dim)
        hidden_states_text = hidden_states_text.to(query.dtype)


        # for ip-adapter
        ip_key_clip = self.to_k_ip(ip_hidden_states_clip) # (batch_size, num_ip_tokens, hidden_size)
        ip_value_clip = self.to_v_ip(ip_hidden_states_clip) # (batch_size, num_ip_tokens, hidden_size)

        # query for ip attention (same as text query)
        # query_ip: (batch_size, num_heads, q_sequence_length, head_dim)
        query_ip = query.view(batch_size, -1, attn.heads, head_dim).transpose(1, 2)

        # ip_key_clip_proj: (batch_size, num_heads, num_ip_tokens, head_dim)
        ip_key_clip_proj = ip_key_clip.view(batch_size, -1, attn.heads, head_dim).transpose(1, 2)
        # ip_value_clip_proj: (batch_size, num_heads, num_ip_tokens, head_dim)
        ip_value_clip_proj = ip_value_clip.view(batch_size, -1, attn.heads, head_dim).transpose(1, 2)


        # --- 修改: 手动计算 IP Attention (scaled dot-product) 并加入温度 ---
        # query_ip: (batch, heads, q_len, head_dim)
        # ip_key_clip_proj: (batch, heads, ip_len, head_dim)
        ip_attention_scores = torch.matmul(query_ip, ip_key_clip_proj.transpose(-1, -2)) / math.sqrt(head_dim)
        
        effective_temperature = torch.clamp(self.ip_temperature, min=1e-2)
        ip_attention_probs = F.softmax(ip_attention_scores / effective_temperature, dim=-1)
        
        # ip_value_clip_proj: (batch, heads, ip_len, head_dim)
        ip_hidden_states_image = torch.matmul(ip_attention_probs, ip_value_clip_proj) # (batch, heads, q_len, head_dim)
        
        ip_hidden_states_image = ip_hidden_states_image.transpose(1, 2).reshape(batch_size, -1, attn.heads * head_dim)
        ip_hidden_states_image = ip_hidden_states_image.to(query.dtype)
        # --- 结束修改 ---
     
        # --- 修改: 使用可学习的融合权重 ---
        learned_scale_factor = torch.sigmoid(self.fusion_alpha) * self.scale
        hidden_states = hidden_states_text + learned_scale_factor * ip_hidden_states_image
        # --- 结束修改 ---

        # linear proj
        hidden_states = attn.to_out[0](hidden_states)
        # dropout
        hidden_states = attn.to_out[1](hidden_states)

        if input_ndim == 4:
            hidden_states = hidden_states.transpose(-1, -2).reshape(batch_size, channel, height, width)

        if attn.residual_connection:
            hidden_states = hidden_states + residual

        hidden_states = hidden_states / attn.rescale_output_factor

        return hidden_states


